                        <h3 class="Subtopico fw-bolder">Memorando Oficial</h3>
                        <!-- subtopico -->

                        <p class="scrool">Até a segunda edição do Manual de Redação Oficial da Presidência da República, havia três tipos de expedientes que se diferenciavam antes pela finalidade do que pela forma: o ofício, o aviso e o memorando. Com o objetivo de uniformizá-los, optou-se por adotar nomenclatura e diagramação únicas, que sigam o que chamamos de padrão ofício.</p>

                        <p class="scrool">No entanto, decidimos mantê-los neste curso, pois eles não deixaram de existir enquanto gênero textual e podem ser solicitados em outras esferas do poder ou em instituições privadas.</p>

                        <!-- Subsubtopico  -->
                        <h4 class="subsubtopico fw-bolder">Conceito</h4>
                        <!-- fim do subsubtopico  -->

                        <p class="scrool">O memorando oficial é uma modalidade de comunicação entre unidades administrativas de um mesmo órgão, que estão hierarquicamente em mesmo nível ou em níveis diferentes. Logo, <strong>trata-se de uma forma de comunicação interna.</strong></p>

                        <p class="scrool">Pode ser utilizado em uma série de situações para tratar de: assuntos administrativos, exposição de projetos, ideias, diretrizes e inovações de serviço, entre outras. Sua principal característica é a agilidade:</p>

                        <!-- Citação com borda verde (esquerda) -->
                        <div class="bd-callout bd-callout-success m-3">

                        <!-- blockquote do bootstrap -->
                        <figure>
                        <blockquote class="blockquote">
                            <p> "a tramitação do memorando em qualquer órgão deve pautarse pela rapidez e pela simplicidade de procedimentos burocráticos."</p>
                        </blockquote>
                        <figcaption class="blockquote-footer">
                            <cite title="Source Title">- BRASIL (2002, p. 18)</cite>
                        </figcaption>
                        </figure>
                        </div>
                        <!-- Citação com borda verde (esquerda) -->

                        <p class="scrool">Nesse sentido, ressalta-se que os despachos ao memorando podem ser dados no próprio documento, o que evita o desnecessário aumento do número de comunicações e de papel.</p>

